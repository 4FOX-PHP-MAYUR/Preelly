export const DEFAULT_COUNTRY_ISO = 'AE';

export const getCountryByIso = (iso) =>
  COUNTRY_CODES.find((country) => country.iso === iso) ||
  COUNTRY_CODES.find((country) => country.iso === DEFAULT_COUNTRY_ISO) ||
  COUNTRY_CODES[0]

export const searchCountries = (query, limit = 12) => {
  const trimmed = String(query || '').trim().toLowerCase()
  if (!trimmed) {
    return COUNTRY_CODES.slice(0, limit)
  }

  const digits = trimmed.replace(/\D/g, '')

  return COUNTRY_CODES.filter((country) => {
    const codeDigits = country.code.replace(/\D/g, '')
    return (
      country.label.toLowerCase().includes(trimmed) ||
      country.iso.toLowerCase().includes(trimmed) ||
      country.code.toLowerCase().includes(trimmed) ||
      (digits && codeDigits.startsWith(digits))
    )
  }).slice(0, limit)
}

export const findCountryByDialCode = (value) => {
  const raw = String(value || '').trim()
  if (!raw) return null

  const withPlus = raw.startsWith('+') ? raw : `+${raw.replace(/\D/g, '')}`
  const digits = withPlus.replace(/\D/g, '')

  const exact = COUNTRY_CODES.filter(
    (country) => country.code === withPlus || country.code.replace(/\D/g, '') === digits
  )

  if (exact.length === 1) {
    return exact[0]
  }

  return null
};

